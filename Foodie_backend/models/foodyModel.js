import mongoose from "mongoose";

const foodySchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  price: {
    type: Number,
    required: true,
  },
  image: {
    type: String,
    required: true,
  },
  category: {
    type: String,
    required: true,
  },
});

const foodyModel = mongoose.model.foody || mongoose.model("foody", foodySchema);

export default foodyModel;
